function renderRelated(related) {
  if (!related || !related.length) return '';
  const items = related.map(function (r) {
    return `          <li><a href="${escapeHtml(r.url)}">${escapeHtml(r.label)}</a></li>`;
  }).join('\n');
  return `
      <div class="blog-related">
        <h3>Related reading</h3>
        <ul>
${items}
        </ul>
      </div>`;
}

