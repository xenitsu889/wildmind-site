function updateSitemap(posts) {
  if (!fs.existsSync(sitemapPath)) {
    console.warn('sitemap.xml not found, skipping');
    return;
  }

  let xml = fs.readFileSync(sitemapPath, 'utf8');

  xml = xml.replace(/\s*<url>\s*<loc>https:\/\/solutions\.wildmindai\.com\/blog\/[^<]+<\/loc>[\s\S]*?<\/url>/g, '');

  const blogEntries = [];
  const today = new Date().toISOString().slice(0, 10);

  blogEntries.push(`  <url>
    <loc>${absoluteUrl('/blog')}</loc>
    <lastmod>${today}</lastmod>
    <changefreq>weekly</changefreq>
    <priority>0.7</priority>
  </url>`);

  posts.forEach(function (post) {
    const mod = post.data.dateModified || post.data.date || today;
    blogEntries.push(`  <url>
    <loc>${absoluteUrl(postUrl(post.data.slug))}</loc>
    <lastmod>${mod}</lastmod>
    <changefreq>monthly</changefreq>
    <priority>0.8</priority>
  </url>`);
  });

  xml = xml.replace('</urlset>', blogEntries.join('\n') + '\n</urlset>');
  fs.writeFileSync(sitemapPath, xml, 'utf8');
  console.log('Updated sitemap.xml with', posts.length + 1, 'blog URLs');
}

function main() {
  const posts = loadPosts();
  console.log('Building', posts.length, 'blog post(s)...');

  if (!fs.existsSync(blogOutDir)) {
    fs.mkdirSync(blogOutDir, { recursive: true });
  }

  const activeSlugs = new Set(posts.map(function (p) { return p.data.slug; }));

  fs.readdirSync(blogOutDir).forEach(function (file) {
    if (!file.endsWith('.html') || file === 'index.html') return;
    const slug = file.replace(/\.html$/, '');
    if (!activeSlugs.has(slug)) {
      fs.unlinkSync(path.join(blogOutDir, file));
      console.log('Removed stale', file);
    }
  });

  posts.forEach(function (post) {
    const outPath = path.join(blogOutDir, post.data.slug + '.html');
    fs.writeFileSync(outPath, renderPostHtml(post), 'utf8');
    console.log('Wrote', outPath);
  });

  fs.writeFileSync(path.join(blogOutDir, 'index.html'), renderBlogIndex(posts), 'utf8');
  console.log('Wrote blog/index.html');

  updateSitemap(posts);
}

main();
