/**
 * Generate blog/*.html, blog/index.html, and update sitemap blog URLs.
 * Source: content/blog/*.md
 */
const fs = require('fs');
const path = require('path');
const matter = require('gray-matter');
const { marked } = require('marked');

const root = path.join(__dirname, '..');
const contentDir = path.join(root, 'content', 'blog');
const blogOutDir = path.join(root, 'blog');
const sitemapPath = path.join(root, 'sitemap.xml');

const SITE_URL = 'https://solutions.wildmindai.com';
const INCLUDE_DRAFTS = process.env.INCLUDE_DRAFTS === '1';

marked.setOptions({ gfm: true, breaks: false });

function escapeHtml(str) {
  return String(str || '')
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');
}

function formatDisplayDate(iso) {
  if (!iso) return '';
  const d = new Date(iso + 'T12:00:00');
  return d.toLocaleDateString('en-GB', { day: 'numeric', month: 'short', year: 'numeric' });
}

function postUrl(slug) {
  return '/blog/' + slug;
}

function absoluteUrl(pathname) {
  return SITE_URL + pathname;
}

function loadPosts() {
  if (!fs.existsSync(contentDir)) return [];

  return fs.readdirSync(contentDir)
    .filter(function (f) { return f.endsWith('.md'); })
    .map(function (file) {
      const raw = fs.readFileSync(path.join(contentDir, file), 'utf8');
      const parsed = matter(raw);
      return {
        data: parsed.data,
        body: parsed.content,
        file: file
      };
    })
    .filter(function (post) {
      if (!post.data.slug || !post.data.title) {
        console.warn('Skipping invalid post:', post.file);
        return false;
      }
      if (post.data.draft && !INCLUDE_DRAFTS) return false;
      return true;
    })
    .sort(function (a, b) {
      return new Date(b.data.date) - new Date(a.data.date);
    });
}

